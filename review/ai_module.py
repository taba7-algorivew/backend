    fail_list = []
    total_list = []
    pass_list = []

    for title, content, status in rentest_list:
        if status == 'fail':
            fail_list.append([title, content])  # ì‹¤íŒ¨í•œ ê²½ìš° fail_listì— ì¶”ê°€
